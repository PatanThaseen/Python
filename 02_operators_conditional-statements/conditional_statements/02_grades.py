marks=int(input("Enter a num:"))
if(marks>=35 and marks<=45):
    print("Grade:E")

elif(marks>=46 and marks<=60):
    print("Grade:D")

elif(marks>=61 and marks<=70):
    print("Grade:C")       

elif(marks>=71 and marks<=80):
    print("Grade:B")

elif(marks>=81 and marks<=90):
    print('Grade:A')

elif(marks>=91 and marks<=100):
    print("Grade:O")

else:
    print("Fail")       
